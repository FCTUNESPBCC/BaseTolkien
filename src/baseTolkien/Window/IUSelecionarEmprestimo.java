    private void btAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddActionPerformed
        if(jTableLivros.getSelectedRow()!=-1){
                modelEmprestimo.addRow(new Object[]{livros.get(jTableLivros.getSelectedRow()).getCodLivro(),
                        livros.get(jTableLivros.getSelectedRow()).getNome()});
                livrosEmprestimo.add(livros.get(jTableLivros.getSelectedRow()));
                                livros.remove(jTableLivros.getSelectedRow());
                model.removeRow(jTableLivros.getSelectedRow());
        }
    }//GEN-LAST:event_btAddActionPerformed

    private void btRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRemoveActionPerformed
        if(jTableLivrosEmprestimo.getSelectedRow()!=-1){
            livros.add(livrosEmprestimo.get(jTableLivrosEmprestimo.getSelectedRow()));
            livrosEmprestimo.remove(jTableLivrosEmprestimo.getSelectedRow());
            modelEmprestimo.removeRow(jTableLivrosEmprestimo.getSelectedRow());
            while(model.getRowCount()>0)
                model.removeRow(0);
            if(!livros.isEmpty())
            for(LivroRelatorio livro: livros){
                if(!livrosEmprestimo.contains(livro))
                    model.addRow(new Object[]{livro.getCodLivro(), livro.getNome()});
            }
        }
    }//GEN-LAST:event_btRemoveActionPerformed


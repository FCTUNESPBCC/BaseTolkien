    private void btPesquisarNomeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPesquisarNomeActionPerformed
        if(livros!=null){
            livros.clear();
            while(model.getRowCount()>0)
            model.removeRow(0);
        }
        livros = BD.getLivroByNomeDisponivel(jTextNome.getText());
        
        for(int cont = 0; cont < livros.size();cont++){
            for(LivroRelatorio livro: livrosEmprestimo){
                if(livro.getCodLivro().equals((livros.get(cont).getCodLivro()))){
                    livros.remove(cont);
                    cont--;
                    break;
                }
            }
        }
        
        if(!livros.isEmpty())
            for(LivroRelatorio livro: livros){
                if(!livrosEmprestimo.contains(livro))
                    model.addRow(new Object[]{livro.getCodLivro(), livro.getNome()});
            }
    }//GEN-LAST:event_btPesquisarNomeActionPerformed

    private void btConfirmarEmprestimoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btConfirmarEmprestimoActionPerformed
        if(!livrosEmprestimo.isEmpty()){
            Emprestimo emprestimo = new Emprestimo(BD.biblioteca.getConfiguracoes().getCodEmprestimo()+1+"", usuario);
            BD.biblioteca.getConfiguracoes().setCodEmprestimo(BD.biblioteca.getConfiguracoes().getCodEmprestimo()+1);
            for(int cont = 0; cont < livrosEmprestimo.size(); cont++){
                emprestimo.getItens().add(new Item(emprestimo.getCodEmprestimo(), livrosEmprestimo.get(cont).getCodLivro(), emprestimo.getDataDevolucao()));
                BD.emprestarLivro(livrosEmprestimo.get(cont).getCodLivro());
            }
            BD.biblioteca.addEmprestimo(emprestimo);
            dispose();
        }
    }//GEN-LAST:event_btConfirmarEmprestimoActionPerformed

    private void btPesquisarCodActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPesquisarCodActionPerformed
        if(livros!=null){
            livros.clear();
            while(model.getRowCount()>0)
            model.removeRow(0);
        }
        else{
            livros = new ArrayList<LivroRelatorio>();
        }
        LivroRelatorio livroTemp = BD.getLivroByCodDisponivel(jTextCodigo.getText());
        if(livroTemp!=null)
            livros.add(livroTemp);
        for(int cont = 0; cont < livros.size();cont++){
            for(LivroRelatorio livro: livrosEmprestimo){
                if(livro.getCodLivro().equals((livros.get(cont).getCodLivro()))){
                    livros.remove(cont);
                    cont--;
                    break;
                }
            }
        }
        if(!livros.isEmpty())
            for(LivroRelatorio livro: livros){
                model.addRow(new Object[]{livro.getCodLivro(), livro.getNome()});
            }
    }//GEN-LAST:event_btPesquisarCodActionPerformed

